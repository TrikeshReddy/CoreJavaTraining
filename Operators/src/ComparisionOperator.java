
public class ComparisionOperator {
	public static void main(String[] args) {
		//boolean b = 10 > 9;
		System.out.println(10 > 9);//true
		System.out.println(10 < 9);//false
		System.out.println(10 > 10);//f
		System.out.println(12 >= 10);//t
		System.out.println(9 <= 10);//t
		//== will check both equal or not 
		//will return true if both are equal
		//will return false if botn are not equal
		System.out.println(6 == 6);//t
		System.out.println(3==6);//f		
		//!=will check both values are not equal
		//will return true if both are not equal
		//will return false if both are equal
		System.out.println(6 != 6);//false
		System.out.println(3!=6);//true
		
		System.out.println(10.98 >12);

	}
}
