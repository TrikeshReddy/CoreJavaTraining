
public class ForLoop {
	public static void main(String[] args) {
		//for syntax
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
			}
		//initilisation will be executed only once before execution of loop starts
		//condition it will be executed till it fails start of every iteration
		//increment/decrement will be executed at the end of every iteration
	}
}
