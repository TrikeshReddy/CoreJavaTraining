
public class SwitchExample {
	public static void main(String[] args) {
		switch (3) {
		case 1:
			System.out.println("value 1");
			break;
		case 2:
			System.out.println("value 2");
			break;
		case 3:
			System.out.println("value 3");
			break;
		default:
			System.out.println("default");
		}
		System.out.println("out");
	}
}
